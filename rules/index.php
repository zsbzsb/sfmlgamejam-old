<h2>SFML Game Jam Rules</h2>

<div style="display: block; float: none; margin: 0px auto; width: 700px;" class="text">
    <br><h3>Time Frame</h3>
    <ul>
        <li>Participants are given seventy-two(72) hours to work on their game.</li>
        <br>
        <li>The theme of the jam will be released one(1) hour prior to the start of the jam.</li>
    </ul>
    <br><h3>Rules</h3>
    <ul>
        <li>You can work on a team of two(2) OR by yourself. Your partner must be stated when submitting your game</li>
        <br>
        <li>You only have seventy-two(72) hours to create both the code and the audio/graphic assets of your project. Pre-made fonts are allowed.</li>
        <br>
        <li>All games must be based on the theme, but how the theme is interpreted and integrated is up to you.</li>
        <br>
        <li>Any programming language may be used, as long as SFML(or one of its bindings) is used.</li>
        <br>
        <li>External libraries are allowed, but not SFML's competitors, such as GLFW, SDL, Allegro, etc. The use of OpenGL/framework libraries is allowed.</li>
        <br>
        <li>Porting to other platforms after the seventy-two(72) hour mark is allowed and encouraged.</li>
    </ul>
    <br><h3>Submissions</h3>
    <ul>
        <br>
        <li>A link to source code is required when submitting.</li>
        <br>
        <li>A link to a playable version of the game for either Windows, Linux, or OS X is required when submitting. This must include the executable and all the assets/dependencies needed to run the game.</li>
        <br>
        <li>Games with NSFW or shocking content must be marked as such.</li>
        <br>
        <li>All submissions must be made within one(1) hour after the jam ends. Updating submission links and such is permitted after the one(1) hour mark.</li>
    </ul>
</div>

